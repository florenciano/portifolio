                        <h3 class="h3 font-weight-normal mt-5">Cardsorting</h3>
                        <p>A partir das análises dos dados dos usuários o passo seguinte realizado foi o <strong>cardsorting</strong> para descobrir como os usuários organizam um conjunto de informações. Também serviu para definir as nomenclaturas e rótulos das funcionalidades do software. O resultado final é um protótipo de uma arquitetura da informação de um sistema. Para a realização desta etapa usou-se a ferramenta online <a href="https://dirtarchitecture.wordpress.com/websort/" title="WebSort | Card Sorting">WebSort</a> (na época do teste o funcionamento da ferramenta limitava-se a 10 usuários/trial).</p>
