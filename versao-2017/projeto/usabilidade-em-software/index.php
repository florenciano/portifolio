                        <p>Resumindo os principais problemas encontrados:</p>
                        <ul>
                            <li><strong>Barra de ferramentas</strong>: <em>Set</em> de iconografia com muitos detalhes e com qualidade duvidosa prejudicando a memorização do usuário e inviabilizando na utilização em tamanhos reduzidos. Ícones repetidos para funções distintas.</li>
                            <li><strong>Arquitetura de informação confusa</strong>: As funcionalidades do software não apresentam um agrupamento organizado. Estas funções são distribuídas, à medida que são desenvolvidas, sem estratégia alguma de alocação.</li>
                            <li><strong>Elementos de interface usados de forma errada</strong>: uso inadequado de componentes de tela para à função desempenhada.</li>
                        </ul>
