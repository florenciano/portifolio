                        <p><small><strong>Auxílio na interpretação do dendograma</strong>: A identificação das relações entre dois itens no diagrama é visualizada através das separações das cores (branco e verde). Uma barra vertical une grupos que representam relações entre si. A leitura fica facilitada se analisada da direita para esquerda. À medida que visão ficar mais à direita teremos uma redução na similaridade entre os grupos, e quanto mais à esquerda a visão se concentrar, maior a similaridade, sendo que cada grupo deverá ter pelo menos dois itens agrupados para gerar relações.</small></p>
